      <p id="parrafo">
        Tomado el control definitivo, los comunistas prohibieron otros partidos políticos, arrestaron personas bajo sospecha de haber colaborado con EE.UU. y los enviaron a campos de reeducación. El gobierno comenzó una intensa campaña de colectivización del campo y las fábricas. La reconstrucción del país por los desastres de la guerra y los serios problemas humanitarios y económicos se dio de manera lenta. Millones de personas huyeron del país en balsas creando una crisis humanitaria internacional.8 9 Entre 1977 y 1978 los jemeres rojos camboyanos atacaron el sur de Vietnam, y ésta respondió invadiendo Camboya. Esta acción empeoró las relaciones de Vietnam con China, la cual lanzó a su vez una breve incursión a Vietnam en febrero de 1979. A su vez, las hostilidades de China hacia Vietnam estrecharon aún más las relaciones de éste con la URSS quien contribuyó una vez más con ayuda militar en armamento.<br>
        En 1986 el Partido Comunista de Vietnam aplicó reformas de libre mercado conocidas como Đổi Mới (Renovación). Con la autoridad del estado permaneciendo inamovible, se promovió la propiedad privada en el campo y las empresas, y la inversión extranjera. La Economía de Vietnam alcanzó entonces un rápido crecimiento en la producción industrial y agraria, la construcción, las exportaciones y la inversión extranjera. En la actualidad es considerada la economía de más rápido crecimiento del mundo.
      </p>
