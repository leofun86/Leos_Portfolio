      <p id="parrafo">
        <b>Ho Chi Min Político:</b> revolucionario y presidente,  su nombre era Nguyen Tat Thanh , pero por sus actividades políticas clandestinas y su activismo tuvo que adoptar diversos nombres, uno de los cuales era Ho Chi Minh ( El luminoso).<br>
        En abril de 1927, Chiang Kai-shek traidor al movimiento comunista, dirige una masacre contra sus dirigentes. Ho Chi Minh logra huir y viaja por Birmania, China y Siam llamando a la revolución en Asia, por lo que pasará largas temporadas en cárceles siendo torturado y cuando está libre, llamando a la huelga y organizando motines y luchas armadas.
      </p>
      <br>
      <img class="img2" src="media/vietnam/01.jpg">
      <br><br>
      <p id="parrafo">
        En Hong Kong, funda el Tanh Nien o Partido Comunista de Vietnam, regresa a Vietnam en 1940 tras ser liberado por las fuerzas aliadas. Se integra, por cinco años, a las guerrillas que luchan contra los invasores japoneses.<br>
        Es ese tiempo que organizará el Vietnam Doc Lap Dong Minh Hoi, o Viet Minh, Liga para la Liberación de Vietnam y crea un ejército guerrillero a cuyo cargo pone a Vo Nguyen Giap, uno de los estrategas militares más brillantes del siglo XX.<br>
        Mientras los vietnamitas intentaban rechazar a los japoneses por el norte, los franceses se preparaban para recuperar lo que consideraban sus territorios coloniales; tras un levantamiento general, se declaró fundada la <b>República Democrática de Vietnam</b>, con base ideológica socialista.
      </p>
      <br>
      <img class="img2" src="media/vietnam/02.jpg">
      <br><br>
      <p id="parrafo">
        En 1950 , las guerrillas llevaron a cabo la <b>Operación Hong-Phong 2</b> que representó una derrota para los franceses y el comienzo del fin de su hegemonía en Vietnam.<br>
        Para 1954 era tanta la presión que Francia pidió ayuda a los Estados Unidos, pero tras la victoria de Dien Bien Phu, los vietnamitas derrotan a los franceses y Ho Chi Minh se convierte en presidente de la República Democrática de Vietnam, que tuvo que esperar nueve años tras su proclamación en 1945 para consolidarse.
      </p>
      <br>
