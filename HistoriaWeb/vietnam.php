      <p id="titulo">INDEPENDENCIA DE VIETNAM</p>
      <p id="parrafo">
         La guerra de Indochina o guerra de Independencia de Indochina fue un conflicto colonial que enfrentó a Francia contra el denominado Viet Minh de Ho Chi Minh que reclamaban la independencia de la Indochina Francesa (Camboya, Laos, Vietnam del Norte y Vietnam del Sur). El conflicto duró de 1946 a 1954.
      </p>
