          <b>Respuesta:</b><br>
          Getulio Vargas: llegó a través de un golpe de estado, realizo cambios profundos para una nueva constitución y código electoral, creo el “Estado Novo” una forma de gobierno parecida a los totalitaristas europeos.<br><br>
          Lázaro Cárdenas: creo en 1936 la confederación de trabajadores de México, Favoreció en incremento salarial que posibilitaría un mayor consumo interno, impulso una política que le aseguraba la plena posesión de los recursos naturales del estado, expropio la industria petrolera pertenecientes a extranjeros lo que lo hizo muy popular en todas las clases sociales.<br><br>
          Juan Domingo Perón: impulso una industrialización basada en el modelo capitalista pero separándose de todos los intereses extranjeros, nacionalizo el  gas natural, petróleo, el carbón y las minas, fue apoyado por el ejercito y los sindicatos. Su esposa Eva Perón, logro el sufragio universal y la creación del partido peronista femenino.
