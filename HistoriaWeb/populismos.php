          <b id="preg"><span style="font-size:35px;">4 </span>Busca información sobre Eva Perón<br>y su actividad durante el gobierno de su esposo</b><br><br>
          <b>Respuesta:</b>
          En su rol de primera dama, desarrollo un trabajo intenso, tanto en lo político, como en lo social. En cuanto a lo político, trabajo intensamente para obtener el voto femenino del peronismo y fue organizadora y fundadora de la rama femenina del peronismo. Esta organización se formo reclutando mujeres de distintas extracciones sociales por todo el país. En lo social, su trabajo se desarrollo en la fundación Perón, mantenida por empresarios y por los propios trabajadores. Creo hospitales, hogares para ancianos y madres solteras, dos policlínicos, escuelas e incluso una ciudad infantil. Durante el año brindaba asistencia a los necesitados y organizaba torneos deportivos infantiles y juveniles. El otro bastión y tal vez eje principal de su popularidad fue construido en torno a los sindicalistas y su facilidad y carisma para conectarse con las masas trabajadoras, a quienes llamaba “descamisados”.
