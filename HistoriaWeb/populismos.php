          <b>Discurso de Perón:</b><br>
          <i style="color:#888;">“Trabajadores: hace casi dos años, desde estos  mismos balcones, dije que tenía tres honras en mi vida: la de ser soldado, la de ser un patriota y la de ser el primer trabajador argentino. Hoy a la tarde, el Poder Ejecutivo ha firmado mi solicitud de retiro del servicio activo del ejército. Con ello, he renunciado voluntariamente al más insigne honor a que puede aspirar un soldado: llevar las palmas y laureles de general de la Nación. Lo he hecho porque quiero seguir siendo el coronel Perón, y ponerme con este nombre al servicio integral del auténtico pueblo argentino. Dejo, pues, el sagrado y honroso uniforme que me entregó la patria para vestir la casaca del civil y mezclarme con esa masa sufriente y sudorosa que elabora el trabajo y la grandeza del país.”</i><br><br>

          <b>Respuesta:</b>
