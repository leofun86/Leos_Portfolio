      <p id="titulo">CONTRAESPIONAJE EN LA GUERRA FRÏA</p>
      <p id="parrafo">
        El contraespionaje o contrainteligencia es la actividad de evitar que el enemigo obtenga información secreta . Está dirigido contra el sistema de espionaje de un enemigo, por ejemplo reclutando agentes en órganos de inteligencia extranjera.
        <br>
        <br>
        Estas actividades fueron realizadas desde 1945 hasta la caída del comunismo, que se dio entre 1989 (Caída del Muro de Berlín) y 1991 (golpe de estado en la URSS).<br>
        Los bloques occidental-capitalista, liderado por Estados Unidos, y oriental-comunista, liderado por la Unión Soviética fueron sus principales protagonistas.
      </p>
        <br>
        <img class="img" src="media/contra/img1.jpg">
        <br>
        <br>
        <?php include 'leer.php'; ?>
        <div class="plegable">
      <p id="parrafo">
        <br>
        Fue una de las actividades más demandadas durante la Guerra Fría. Este enfrentamiento tuvo lugar a los niveles político, ideológico, económico, tecnológico, militar e informativo.
        <br>
        <br>
        Los dos bloques tenían agencias que respondían a esta actividad, por parte del bloque comunista la KGB y por parte del bloque capitalista la CIA.
        <br>
        <br>
        Otro de las agencias de contrainteligencia más importantes fue el Servicio de Inteligencia Secreto (SIS), conocido como MI6  que es la agencia de inteligencia externa del Reino Unido.
        <br>
        <br>
        <b>KGB</b><br>
        <img src="media/contra/kgb.jpg"><br>
        Fue el nombre de la agencia de inteligencia, así como de la agencia principal de policía secreta de la Unión Soviética.
        Se encargó de obtener y analizar toda la información de inteligencia de la nación, pero se convirtió en un organismo de represión de toda la Unión Soviética.<br>
        Desapareció junto con la URSS y partir de allí surgió el Servicio de Inteligencia Extranjera, el cual pasó a dirigir las actividades de espionaje fuera del país  informando directamente al presidente del Gobierno.
        <br>
        <br>
        <b>MI6</b><br>
        <img src="media/contra/mi6.jpg"><br>
        Creado durante la Primera Guerra Mundial obtuvo su primeras operaciones exitosas en inteligencia militar y comercial a través de agentes en países neutrales.<br>
        El nombre MI6, que es prácticamente un sinónimo de SIS comenzó a aplicarse en 1930 y se mantuvo durante la Segunda Guerra Mundial.
        La existencia del SIS no fue reconocida oficialmente hasta 1994.<br>
        A finales de 2010, el jefe del SIS entregó lo que dijo que fue el primer discurso público por un jefe de servicio de la agencia en sus 101 años de historia.
        <br>
        <br>
        <b>CIA</b><br>
        <img src="media/contra/cia.jpg"><br>
        La CIA fue creada el 18 de diciembre de 1947 por el Presidente de EE.UU, Harry S. Truman sustituyendo a la Oficina de Servicios Estratégicos (OSS) de la II Guerra Mundial.<br>
        Utilizando procedimientos y agentes de la Organización de Servicios Especiales fue creada durante la guerra para cumplir misiones de espionaje y apoyo a la resistencia tras las líneas alemanas.<br>
        En 1949 se le otorgan poderes para investigar sin necesidad de autorización judicial, expedientes administrativos y fiscales.
        <br>
        <br>
        <b style="font-size:21px;">Causas fundamentales de su creación:</b><br>
        - Ataque sobre la flota norteamericana en Pearl Harbor por parte de Japón.<br>
        - Consolidación de EUA como la gran potencia mundial transformándose en el acreedor principal en la recuperación económica europea.
        <br>
        <br>
        <b>MATA HARI</b><br>
        <img src="media/contra/matahari.jpg"><br>
        Era una bailarina exótica y prostituta de clase alta en París que espió para Alemania durante la Primera Guerra Mundial.
        <br>
        <br>
        Su pasaporte holandés (Holanda era un país neutral) le permitió literalmente, dormir con el enemigo y trabajar para la Inteligencia del ejército alemán.<br>
        La Inteligencia francesa interceptó una señal de radio codificada de Berlín develando el envío de información de un espía alemán con nombre en código H-21.<br>
        Mata- Hari fue identificada y detenida en una habitación de hotel en París el 13 de febrero de 1917 y fusilada posteriormente durante el mismo año.
        <br>
        <br>
        <b>IAN FLEMING</b><br>
        <img src="media/contra/fleming.jpg"><br>
        Fue reclutado  como asistente hasta llegar a comandante y concibió un plan que nunca fue ejecutado ( operación Ruthless).
        La misión era capturar la máquina codificadora Enigma, usada en las comunicaciones de la armada alemana.
        <br>
        <br>
        Su trabajo como asistente en los servicios secretos permitió  imbuirse del ambiente que se respira en sus novelas de espionaje.  La primera novela que puso en escena a James Bond fue Casino Royale  publicada en 1953.
        <br>
        <br>
        <b>ARMAS SECRETAS</b>
        <br>
        <br>
        <b>Carbón explosivo – USA (1942 – 1945)</b><br>
        <img src="media/contra/carbon.jpg"><br>
        El explosivo camuflado era utilizado en operaciones de sabotaje, principalmente en trenes y barcos. Simulaba ser un pedazo de carbón y era introducido en los hornos que hacían mover barcos y trenes provocando explosiones.
        <br>
        <br>
        <b>F21- URSS (1970)</b><br>
        <img src="media/contra/f21.jpg"><br>
        Los rusos usaban la cámara F21 para obtener fotos clandestinas. La máquina se ocultaba detrás de un botón falso que se abrÍa para fotografiar. El disparador estaba en uno de los bolsos del abrigo, que se conectaba por un cable.
        <br>
        <br>
        <b>Reloj- Alemania (1949)</b><br>
        <img src="media/contra/reloj.jpg"><br>
        En su interior hay una película circular suficiente para sacar hasta 6 fotografías mientras se "mira" la hora.
        <br>
        <br>
        <b>Maletín- Alemania Oriental ( 1980)</b><br>
        <img src="media/contra/maletin.jpg"><br>
        Para leer mensajes secretos los agentes del gobierno usaban bolígrafos conteniendo un tipo especial de tinta que sólo podía ser vista en el papel cuando se sometía a rayos ultravioletas emitidos sobre ciertas condiciones. La maleta tenía todo lo necesario para descifrar los mensajes.
        <br>
        <br>
        <b>Zapato- URSS (1960)</b><br>
        <img src="media/contra/zapato.jpg"><br>
        Ideado por agentes soviéticos para escuchar conversaciones secretas durante la guerra fría. El transmisor, micrófono y las baterías se instalaban entre la suela y el tacón del zapato. Alguien próximo a la persona investigada (empleado de hotel, por ejemplo), instalaba el sistema en los zapatos. Todo lo que la persona hablaba pasaba entonces ha ser transmitido a una estación de escuchas.
