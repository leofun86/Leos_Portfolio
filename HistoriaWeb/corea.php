      <p id="parrafo">
        La península de Corea fue dividida por el paralelo 38  fue invadida por tropas de la U.R.S.S. al norte y tropas de E.E.U.U. al sur. Cuando comienza la Guerra Fría, en 1948 en Corea del Norte se implanta un régimen comunista opuesto al régimen pro-norteamericano del sur.<br>
        Un año más tarde, se implanta en China otro régimen comunista que lleva a Stalin a permitir un ataque de Corea del Norte sobre Corea del Sur. Así el 25 de junio de 1950, las tropas de Corea del Norte atraviesan triunfantes la frontera.
      </p>
      <br>
      <img class="img2" src="media/corea/01.jpg">
      <br><br>
      <p id="parrafo">
        En un par de meses, Corea del Sur apenas se ha podido defender y el enemigo está en torno a Pusan, capital de estos. Sus aliados Estados Unidos y la ONU responden rápido en su auxilio y con el general Douglas MacArthur como jefe supremo de las fuerzas desembarcan en la zona de Inchon, con el objetivo de liberar la presión sobre la capital surcoreana, fue una operación muy arriesgada pero que tuvo un tremendo éxito que hizo retroceder al ejército norcoreano hasta el paralelo 38 de nuevo.
      </p>
      <br>
      <img class="img2" src="media/corea/02.jpg">
      <br><br>
      <p id="parrafo">
        La misión estadounidense parecía haber acabado, pero a la mayoría de los mandos les pareció buena idea seguir adentrándose en Corea del Norte para poder unirla bajo el poder de Corea del Sur, fue entonces cuando la victoria estaba cerca, la nueva República Popular de China entro en combate con más de 300.000 hombres que hicieron retroceder al ejército de las Naciones Unida. El ejército China iba armado con armamento proporcionado por la URSS. En este momento el general MacArthur pidió el bombardeo atómico sobre China, por ello fue destituido a favor del general Ridgway.
      </p>
      <br>
      <img class="img2" src="media/corea/03.jpg">
      <br><br>
      <p id="parrafo">
        El ejército chino recupero el 4 de enero de 1951 la ciudad de Seúl. En el invierno las tropas de las Naciones Unidas sufrieron una grave derrota y tuvieron que retirarse una vez más al paralelo 38, en estos momentos la guerra se estancó y hasta 1953 hubo pequeños avances y retrocesos de las distintas fuerzas en este punto.
      </p>
      <br>
      <img class="img2" class="img2" src="media/corea/04.jpg">
      <br><br>
      <p id="parrafo">
        Finalmente tras el empate militar se abrieron las negociaciones de paz para acabar el conflicto, en julio de 1953 tras la muerte de Stalin, se acordó el armisticio de Panmunjong en el que se establecía un perímetro desmilitarizado en torno al paralelo 38. La perduración del régimen comunista en Corea del Norte ha hecho que sea la única “frontera de la Guerra Fría” que existe hoy día.  
        Las bajas en ambos bandos fueron altas; Naciones Unidas, 36.000 norteamericanos muertos más heridos, 400.000 surcoreanos heridos y muertos; China y Corea del Norte, 1.500.000 entre heridos y muertos.
      </p>
