        <b>Carrera Política de Salvador Allende</b><br>
        - Fue uno de los fundadores del Partido Socialista de Chile (1933).El Partido Socialista de Chile (PS) es un partido político chileno fundado el 19 de abril de 1933. Fundado inicialmente como un partido marxista que acogía a una pluralidad de corrientes de la izquierda política.<br>
        - En 1937 fue elegido diputado por Valparaíso y Quillota.<br>
        - En 1939, asumió como ministro de Salubridad, Previsión y Asistencia Social del gobierno de Pedro Aguirre Cerda.<br>
        - Entre 1945 y 1969 fue electo senador.<br>
        - Entre 1951 y 1955 fue vicepresidente de la Cámara Alta.<br>
        - Presidente entre los años 1966 y 1969.<br>
        - Candidato a la presidencia de la República en las elecciones de 1958 y 1964.<br>
        - 1970, fue electo primer mandatario, cargo que ocupó hasta el golpe de Estado del 11 de septiembre de 1973, día en que se suicidó.
      </p>
