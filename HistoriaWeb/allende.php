      <p id="parrafo">
        El 4 de septiembre de 1970, Allende obtuvo el 36,6 por ciento de los votos. Seguido del candidato de la derecha, Jorge Alessandri, con un 34,9 por ciento, y del abanderado de la Democracia Cristiana (DC), Radomiro Tomic, con un 27,8 por ciento. Como no hubo mayoría absoluta, el Congreso pleno tuvo que decidir.<br><br>
        <b>A la gestión Allende se la caracterizó por abanderar proyectos como:</b><br>
        <b>2.</b> Reforma agraria: La mayor parte del terreno cultivable de Chile eran latifundios en manos de un puñado de familias. El gobierno de Allende promulgó una reforma agraria que prohibía la posesión de más de 80 hectáreas por persona. En 18 meses todos los latifundios desaparecieron. Además, los campesinos, a través de organizaciones cooperativistas, reemplazarían a los representantes de los latifundistas en todos los organismos del Estado. También se instauró una asistencia técnica gratuita al campesinado y se establecieron planes de crédito para las nuevas cooperativas.<br>
        <b>1.</b> Nacionalización de la minería del cobre y la banca: El cobre ha sido siempre un producto esencial de la economía chilena. En los años ‘70, el cobre representaba tres cuartas partes de todas las exportaciones del país. Cuando Allende llega al poder, el Estado posee el 51% de las principales minas de cobre. El resto pertenece a compañías estadounidenses, en particular Anaconda Copper Company, controlada por las poderosas familias Rothchild y Rockefeller.<br>
        <b>3.</b> Reformas sanitarias, educativas y sociales: Durante sus 3 años de gobierno, Allende promulgó además una serie de reformas en el ámbito sanitario, educativo y social: Universidad gratuita. El número de estudiantes universitarios creció un 89% entre 1970 y 1973. Para muchas familias humildes era la primera vez que sus hijos podían acceder a estudios superiores. Sistema de becas para los niños de descendencia indígena (fundamentalmente mapuches) que habían sido discriminados durante décadas. El “Programa de Suplementos Alimenticios” fue extendido a todos los niños en escuelas primarias y a todas las mujeres embarazadas. Aumento de las pensiones mínimas al doble de la inflación. Instauración de un sistema de centros de salud en barrios obreros con al menos un centro de salud por cada 40.000 habitantes.
      </p>
