      <p id="parrafo">
        El 22 de octubre fue víctima de un atentado el comandante en jefe del Ejército, René Schneider, para evitar la llegada de Allende a la presidencia, Roberto Viaux, junto a miembros de Patria y Libertad planearon el secuestro de Schneider, con el fin de provocar la intervención de las fuerzas armadas y evitar la sesión del Congreso, René falleció dos días después. Este fue un intento por impedir que Allende asumiera el poder. Sin embargo, el Parlamento lo ratificó como Presidente de la República el 24, siendo el primer candidato marxista que llegaba a ese cargo por votación popular, en la historia del mundo occidental.
      </p>
