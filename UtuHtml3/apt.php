			<h2>+ TEXTO</h2>
			<div id="tab1" class="plegable">
				<br>
				<ul>
					<li><b>Definición:</b> Define en su etimología del vocablo latino "Textus" que significa "Algo tejido o Entrelazado".<br>
					Este entramado define a un texto como una organización de una serie de enunciados que tengan ciertas condiciones que los hacen inteligibles.<br>
					No importa la extensión; Un texto puede ser una sola oración o un libro, o un poema, o una publicidad. Puede ser oral o escrito.<br>
					Las condiciones para que un conjunto de enunciado sea considerado texto son:
						<ul>
							<ul>
								<br>
								<li>Coherencia (semántica)</li>
