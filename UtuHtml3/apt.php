			<h2>+ DEFINICIONES VARIAS</h2>
			<div id="tab1" class="plegable">
				<br>
				<ul>
					<li><b>Texto: </b>unidad linguística emitida por un hablante con una situación comunicativa concreta y con una finalidad determinada.</li><br>
					<li><b>Coherencia: </b>propiedad que nos permite entender el texto como una unidad comunicativa y no como una sucesión de enunciados inconexos. Se manifiesta en tres niveles: La totalidad del texto (cohesión global), sus partes constitutivas (coherencia lineal) y los diversos enunciados que los componen (cohesión local).</li><br>
					<li><b>Cohesión: </b>Red de relaciones entre los distintos elementos y mecanismos que manifiestan.</li><br>
					<li><b>Adecuación: </b>Característica de los textos que están bien sontruídos desde el punto de vista comunicativo. Se debe tener en cuenta los diferentes factores o elementos que intervienen en la comunicación: Quién es el emisor, cual es su intensión comunicativa, quién es el receptor, qué relación hay entre ambos y cuál es el canal que se va a utilizar.</li><br>
					<li><b>Texto narrativo: </b>Un texto narrativo es aquel que presenta una sucesión de un estado de cosas inicial. Esos hechos mantienen entre sí alguna relación que les da el carácter de unidad. Generalmente, el orden de los acontecimientos es relevante para una narración: No da lo mismo si primero se cometió u crimen si primero llegó la policía o, si a la inversa, primero llegó la policía y luego se cometió el crimen. En efecto, el orden temporal suele ser correlativo con un orden lógico o causal entre los hechos en la medida en que, para que un acontecimiento sea causa del otro, es condición necesaria que haya ocurrido previamente. Una división fundamental de las narraciones está dada por si se presupone que los hechos relatados son reales o no. Los relatos periodísticos o el recurso histórico se refieren a hechos que (se supone) sucedieron con la realidad y que de algún modo pueden ser comprobados materialmente a partir de evidencia como documentos escritos, fotografías, documentos directos, etc. Por el contrario los relatos ficcionales no pretenden relatar hechos que ocurrieron realmente, sino que son creación de un escritor, aún cuando incorporan elementos (hechos, personajes, lugares) del mundo real.</li>
				</ul>
