			<h2>+ DELIMITAR UNIDADES COORDINADAS</h2>
			<div id="tab1" class="plegable">
			<br>
			<ul>
				<li><b>Se usa para separar miembros gramaticalmente equivalentes dentro de un mismo enunciado</b>
					<ul>
						<li><span class="fa">Salvo que exista una conjunción: e, y, o, u.</span>
							<ul>
								<li>Ejemplos: Corrieron, saltaron, jugaron y gritaron toda la vida.</li>
							</ul>
						</li>
					</ul>
				</li>
				<br>
				<li><b>Sin embargo, se coloca una coma delante de la conjunción cuando</b>
					<ol>
						<li>La secuencia que encabeza expresa su contenido distinto al  elemento anterior.</li>
						<li>La conjunción destinada a enlazar con toda la proposición anterior y no con el último de sus miembros.<br><br>
							<ul>
								<li>Ejemplo: Pagó el traje, el bolso y los zapatos, y salió de la tienda.</li>
							</ul>
						</li>
					</ol>
				</li>
				<br>
				<li><b>Es recomendable su uso cuando el período es especialmente largo =></b> En una relación cuyos elementos están separados por punto y coma:
					<ul>
						<ul>
							<li>En el armario colocó la vajilla; En el cajón, los cubiertos; En los estantes, los vasos, y los alimentos, en la despensa.</li>
						</ul>
					</ul>
				</li>
			</ul>
